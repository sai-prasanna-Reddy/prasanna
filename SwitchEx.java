public class SwitchEx{  
public static void main(String[] args) {  
    int number=35;     
    switch(number){     
    case 25: 
      System.out.println("25");  
      break;  
    case 35: 
      System.out.println("35");  
      break;  
    case 45: 
      System.out.println("45");  
      break;    
    default:
      System.out.println("Not in 25, 35 or 45");  
    }  
}  
}  