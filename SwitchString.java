class SwitchString
{
 public static void main(String args[])
 {
   String str="hii";
   switch(str)
   { 
    case "hii":
       System.out.println("hii");
       break;
    case "hello":
        System.out.println("hello");
        break;
    default:
        System.out.println("nothing");
     }
   }
}